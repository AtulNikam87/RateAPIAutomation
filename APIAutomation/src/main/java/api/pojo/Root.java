package api.pojo;

public class Root {
    public String base;
    public Rates rates;
    public String date;
    
	public String getBase() {
		return base;
	}
	public void setBase(String base) {
		this.base = base;
	}
	public Rates getRates() {
		return rates;
	}
	public void setRates(Rates rates) {
		this.rates = rates;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
